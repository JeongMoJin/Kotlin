// Range
val range1 = 1..10
val range2 = 1 until 10
println(range1)
println(range2)
val range3 = 'A'..'Z' // 코틀린은 언어의 순서도 알고있다
println(range3)

// Progression
val range4 = 1..10 step 2 // step = 2씩 증감돼서 출력
val range5 = 10 downTo 1 step 2
println(range4)
println(range5)
// STEP
// - 특징 : step에 값과 상관없이 첫번째는 무조건 index 0 부터 시작한다

